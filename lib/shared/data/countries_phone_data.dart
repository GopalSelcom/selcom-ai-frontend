  /// ISO 3166-1 alpha-2 (e.g. `TZ`, `IN`).
  static CountryData findByIsoCode(String? iso2) {
    final u = (iso2 ?? 'TZ').trim().toUpperCase();
    try {
      return all.firstWhere((c) => c.code == u);
    } catch (_) {
      return all.firstWhere((c) => c.code == 'TZ');
    }
  }

  /// `dialCode` like `+255` or `255`.
  static CountryData findByDialCode(String dialCode) {
    final normalized =
        dialCode.startsWith('+') ? dialCode : '+${dialCode.replaceAll('+', '')}';
    try {
      return all.firstWhere((c) => c.dialCode == normalized);
    } catch (_) {
      return findByIsoCode('TZ');
    }
  }
